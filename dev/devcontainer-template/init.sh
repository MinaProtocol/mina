#!/usr/bin/env bash
#
# initializeCommand for .devcontainer/mina/devcontainer.json — runs on the
# host BEFORE docker compose starts. Writes dev/.env with the host user's
# UID/GID so docker-compose's ${HOST_UID}/${HOST_GID} substitutions resolve
# when VS Code launches compose. (The `make` path covers this via Makefile
# exports; this script is the equivalent shim for the VS Code path.)
#
# Path math: this script lives at .devcontainer/mina/init.sh once installed,
# and at dev/devcontainer-template/init.sh in the source tree. Both are two
# directories below the repo root, so the same `../..` traversal works for
# either location.

set -euo pipefail

cd "$(dirname "${BASH_SOURCE[0]}")/../.."

uid=$(id -u)
gid=$(id -g)

cat > dev/.env <<EOF
# Auto-generated by .devcontainer/mina/init.sh — do not edit by hand.
# Consumed by dev/docker-compose.yml so the in-container opam user can
# be remapped to your host UID/GID.
HOST_UID=${uid}
HOST_GID=${gid}
EOF

echo "[.devcontainer/mina/init.sh] wrote dev/.env (HOST_UID=${uid} HOST_GID=${gid})"
